    public boolean checkfields(){
        if ((this.dia.getText().equals("")) || (this.mes.getText().equals("")) || (this.ano.getText().equals("")) || this.valor.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campos obrigatórios não foram preenchidos!");
            return false;
        }
        return true;
    }   
