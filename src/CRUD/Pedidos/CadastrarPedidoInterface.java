    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //CADASTRAR
        if(checkfields()){
            String datapedido = this.dia.getText() + "/" + this.mes.getText() + "/" + this.ano.getText();
            Pedido pedido = new Pedido(this.nomeprod.getSelectedItem().toString(), this.nomefab.getText(), this.model.getText(), this.quant.getText(), this.valor.getText(), datapedido,this.obs.getText());
            if (PedidoController.CadastrarPedido(pedido)){
                JOptionPane.showMessageDialog(null, "Cadastro Realizado com sucesso!");
            }

            else{
                JOptionPane.showMessageDialog(null, "Erro!");
            }
            
            this.dispose();
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

