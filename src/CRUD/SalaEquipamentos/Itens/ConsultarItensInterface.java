    public ConsultarItensInterface() {
        initComponents();
        DefaultTableModel listModel = (DefaultTableModel) this.table.getModel();
        List itens = ItemController.ListarItens();
        Iterator itensx = itens.iterator();
        while(itensx.hasNext()){
            Item itemx = (Item) itensx.next();
            if (itemx.getClassificacao()==1){
                listModel.addRow(new Object[]{itemx.getNome(), itemx.getLocalizacao(), "Freezer"});
            }
            else if (itemx.getClassificacao()==2){
                listModel.addRow(new Object[]{itemx.getNome(), itemx.getLocalizacao(), "Sala"});
            }
            else if (itemx.getClassificacao()==3){
                listModel.addRow(new Object[]{itemx.getNome(), itemx.getLocalizacao(), "Equipamento"});
            }
}
    }

